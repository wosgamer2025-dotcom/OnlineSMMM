function normalizeLocaleContent(locale, code) {
  const sharedCopy = localizedSharedCopy[code] || localizedSharedCopy.en;
  if (code === 'tr') {
    return {
      ...locale,
      ...sharedCopy,
    };
  }

  const testimonialCopy = testimonialFallbackCopy[code] || testimonialFallbackCopy.en;
  const testimonials = tr.testimonials.map((base, index) => {
    const existing = Array.isArray(locale.testimonials) ? locale.testimonials[index] : null;
    const caseData = testimonialCaseData[index % testimonialCaseData.length];
    const pattern = testimonialCopy.patterns[index % testimonialCopy.patterns.length];
    const quote = existing && locale.testimonials !== tr.testimonials && existing.quote
      ? existing.quote
      : `${testimonialCopy.sector}: ${caseData[0]}. ${testimonialCopy.companyType}: ${caseData[1]}. ${testimonialCopy.duration}: ${caseData[2]}. ${testimonialCopy.result}: ${caseData[3]}. ${pattern}`;

    return {
      quote,
      name: existing?.name || base.name,
      role: existing?.role || caseData[0],
      country: existing?.country || base.country,
      photo: existing?.photo || base.photo,
    };
  });

  const faqFallbacks = faqFallbackCopy[code] || faqFallbackCopy.default;
  const faqs = tr.faqs.map((_, index) => {
    const existing = Array.isArray(locale.faqs) ? locale.faqs[index] : null;
    if (existing?.question && existing?.answer) {
      return existing;
    }

    const fallback = faqFallbacks[index % faqFallbacks.length];
    return { question: fallback[0], answer: fallback[1] };
  });

  return {
    ...locale,
    ...sharedCopy,
    testimonials,
    faqs,
  };
}

const rawLocales = { tr, en, de, it, es, fr, az, ky, tk, ru, ar };
const locales = Object.fromEntries(Object.entries(rawLocales).map(([code, locale]) => [code, normalizeLocaleContent(locale, code)]));

